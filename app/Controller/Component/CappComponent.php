    public function getCleanText($str = '')
    {
        if($str == '') return $str;
        
        $str = strip_tags($str);
        $str = preg_replace('/[\r\n]/', ' ', $str);
        $str = preg_replace('/\ +/', ' ', $str);
        $str = trim($str);
        
        return $str;
    }
    
    public function wordLimiter($str, $limit)
    {
        $str = strip_tags($str);
        $str = preg_replace('/[\r\n]/is', ' ', $str);
        $str = preg_replace('/\ +/', ' ', $str);
        $str = trim($str);

        if($str == '') return $str;

        $arr = explode(' ', $str);
        if(count($arr) <= $limit) return $str;

        $buff = array();

        for($i=0; $i<$limit; $i++)
        {
            $buff[] = $arr[$i];
        }

        return implode(' ', $buff) . '...';
    }

    public function curl_html($url, $referer="", $timeout = 300, $header = 0, $test = 0)
    {
        $curl = curl_init(); // Khởi tạo

        if(strstr($referer,"://"))
            curl_setopt ($curl, CURLOPT_REFERER, $referer); //Giả REFERER
        
        curl_setopt ($curl, CURLOPT_URL, $url); // Chỉ định địa chỉ lấy dữ liệu
        curl_setopt ($curl, CURLOPT_TIMEOUT, $timeout); // Thiết lập timeout
        curl_setopt ($curl, CURLOPT_USERAGENT, 'Mozilla/5.0 (compatible; Googlebot/2.1; +http://www.google.com/bot.html)');
        // curl_setopt ($curl, CURLOPT_USERAGENT, sprintf("Mozilla/%d.0",rand(4,5)));// Giả tên trình duyệt
        curl_setopt ($curl, CURLOPT_HEADER, (int)$header); // Có kèm header của HTTP Reponse trong nội dung phản hồi ko
        curl_setopt ($curl, CURLOPT_RETURNTRANSFER, 1); // Tham số bổ sung
        curl_setopt ($curl, CURLOPT_SSL_VERIFYPEER, 0); // Tham số bổ sung, ko xác nhận chứng chì sslc
        curl_setopt ($curl, CURLOPT_SSL_VERIFYHOST, 0);
        curl_setopt ($curl, CURLOPT_FOLLOWLOCATION, TRUE);

        $html = curl_exec ($curl);
        $httpcode = curl_getinfo($curl, CURLINFO_HTTP_CODE);
        
        curl_close ($curl);

        if($test != 0)
            return $httpcode . $html;
        
        return $httpcode == '200' ? $html : $httpcode;
    }
    
